'''
1. Ассоциация
Ассоциация — это общее отношение, когда один объект "знает" о другом и может с ним взаимодействовать. 
Объекты связаны, но их существование полностью независимо друг от друга. Ассоциация обычно описывает 
поведение без обязательной структурной связи "часть-целое".

Пример: Учитель и Курс. Учитель "знает" о курсах, которые он ведёт, и курс "знает" о своём учителе. 
Но если курс отменят или учитель уйдёт, другой объект останется существовать. 
Здесь нет жёсткой зависимости, и объекты не являются частями друг друга.

2. Агрегация (тип "часть-целое")
Агрегация — это специальный вид ассоциации, где один объект выступает "целым", а другие — его "частями". 
Однако, в отличие от композиции, "части" могут существовать независимо от "целого". 
Здесь связь более тесная, чем при ассоциации, но не до уровня "жизненной зависимости".

Пример: Университет и Студенты. Университет "состоит" из студентов, но студенты могут существовать вне университета. 
Если университет закроется, студенты останутся. Это "часть-целое", но не столь жёсткое, как в композиции.

3. Композиция (тип "часть-целое")
Композиция — это самая тесная связь "часть-целое", где "части" полностью зависят от "целого" 
и не могут существовать отдельно от него. Если уничтожается "целое", уничтожаются и все его "части".

Пример: Дом и Комнаты. Дом "состоит из" комнат, и если дом разрушится, 
комнаты перестанут существовать, потому что они не могут существовать без дома.

4. Зависимость
Зависимость — это временная связь, когда один объект использует другой для выполнения своих действий, 
но не хранит его как часть себя и не зависит от его существования постоянно. 
Зависимость предполагает слабую связь, которая существует только в момент использования.

Пример: Класс Document использует Printer для печати. Document "зависит" от Printer, 
так как для выполнения печати он вызывает метод Printer, но Printer 
не является частью Document, и Document может существовать без Printer.
'''