create table Company(
	id INTEGER PRIMARY KEY AUTOINCREMENT,
	name TEXT
);

create table Trip(
	id INTEGER PRIMARY KEY AUTOINCREMENT,
	company INTEGER,
	plane TEXT,
	town_from TEXT,
	town_to TEXT,
	time_out TEXT,
	time_in TEXT,
	FOREIGN KEY (company) REFERENCES Company(id)
);

create table Passeger(
	id INTEGER PRIMARY KEY AUTOINCREMENT,
	name TEXT
);

create table Pass_in_trip(
	id INTEGER PRIMARY KEY AUTOINCREMENT,
	trip INTEGER,
	passeger INTEGER,
	place TEXT,
	FOREIGN KEY (trip) REFERENCES Trip(id),
	FOREIGN KEY (passeger) REFERENCES Passeger(id)
);


